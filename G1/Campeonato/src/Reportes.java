    private void btGenerarJugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGenerarJugaActionPerformed
        
        try {
            conectar cc=new conectar();
            java.sql.Connection cn=cc.conexion();
            
            
            JasperReport reporte = null;
            String path = "src\\Reporte\\JUGADORES.jasper";
            
            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);
            
            
            JasperPrint jprint = JasperFillManager.fillReport(reporte, null,cn);
            
            JasperViewer view = new JasperViewer(jprint, false);
            
            
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            
            view.setVisible(true);
            
            
        } catch (JRException ex) {
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btGenerarJugaActionPerformed

